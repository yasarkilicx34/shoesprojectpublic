    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String code = code_field.getText();
        String name = name_field.getText();
        String number = number_field.getText();
        String type = type_field.getText();
        String stock = stock_field.getText();
        
        int selectedrow = shoes_table.getSelectedRow();
        
        if(selectedrow == -1){
            if(model.getRowCount() == 0){
                message2.setText("Shoes Table null");
            }
            else{
                message2.setText("Shoes select update!");
            }
        }
        else{
            int id = (int)model.getValueAt(selectedrow,0);
            op.bookUpdate(id,code,name,number,type,stock);
            shoesView();
            message1.setText("Shoes update succesfull!");
        }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

