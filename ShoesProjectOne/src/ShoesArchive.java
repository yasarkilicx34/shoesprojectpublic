    public void shoesView(){
        model.setRowCount(0);
        ArrayList<Shoes> shoes = new ArrayList<Shoes>();
        shoes = op.ShoesCome();
        
        if(shoes != null){
            for(Shoes shoesx : shoes){
                Object[] add ={
                    shoesx.getId(),shoesx.getShoes_code(),shoesx.getShoes_name(),shoesx.getShoes_number(),shoesx.getShoes_type(),shoesx.getShoes_stock()
                };
                model.addRow(add);
                
            }
            
        }
        
    }
